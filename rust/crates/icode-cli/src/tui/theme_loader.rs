/// Find a theme by its ID.
pub fn find_theme(id: &str) -> Option<&Theme> {
    THEMES.iter().find(|e| e.id == id).map(|e| e.theme)
}

/// List all theme IDs.
pub fn list_themes() -> Vec<&'static str> {
    THEMES.iter().map(|e| e.id).collect()
}

/// List themes grouped by category.
pub fn list_themes_by_category() -> Vec<(ThemeCategory, Vec<&'static ThemeEntry>)> {
    use ThemeCategory::{Colorful, Dark, Default, Light, Popular, AMOLED};
    let order = [Default, Popular, Dark, Light, AMOLED, Colorful];
    order
        .into_iter()
        .map(|cat| {
            let entries: Vec<&'static ThemeEntry> = THEMES
                .iter()
                .copied()
                .filter(|e| e.category == cat)
                .collect();
            (cat, entries)
        })
        .filter(|(_, entries): &(ThemeCategory, Vec<&'static ThemeEntry>)| !entries.is_empty())
        .collect()
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn find_theme_returns_some_for_known_ids() {
        assert!(find_theme("opencode").is_some());
        assert!(find_theme("catppuccin-mocha").is_some());
        assert!(find_theme("dracula").is_some());
        assert!(find_theme("github-light").is_some());
        assert!(find_theme("amoled").is_some());
    }

    #[test]
    fn find_theme_returns_none_for_unknown() {
        assert!(find_theme("nonexistent").is_none());
        assert!(find_theme("").is_none());
    }

    #[test]
    fn list_themes_returns_all_37() {
        let themes = list_themes();
        assert_eq!(themes.len(), 37);
    }

    #[test]
    fn list_themes_contains_expected_ids() {
        let themes = list_themes();
        assert!(themes.contains(&"opencode"));
        assert!(themes.contains(&"catppuccin-mocha"));
        assert!(themes.contains(&"nord"));
        assert!(themes.contains(&"github-light"));
        assert!(themes.contains(&"amoled"));
        assert!(themes.contains(&"synthwave-84"));
    }

    #[test]
    fn list_themes_by_category_groups_correctly() {
        let grouped = list_themes_by_category();
        let categories: Vec<_> = grouped.iter().map(|(c, _)| c.label()).collect();

        assert!(categories.contains(&"Default"));
        assert!(categories.contains(&"Popular"));
        assert!(categories.contains(&"Dark"));
        assert!(categories.contains(&"Light"));
        assert!(categories.contains(&"AMOLED"));
        assert!(categories.contains(&"Colorful"));
    }

    #[test]
    fn category_counts_sum_to_37() {
        let grouped = list_themes_by_category();
        let total: usize = grouped.iter().map(|(_, entries)| entries.len()).sum();
        assert_eq!(total, 37);
    }

    #[test]
    fn default_category_has_3_themes() {
        let grouped = list_themes_by_category();
        let default_entry = grouped
            .iter()
            .find(|(c, _)| matches!(c, ThemeCategory::Default));
        assert!(default_entry.is_some());
        let (_, entries) = default_entry.unwrap();
        assert_eq!(entries.len(), 3);
    }

    #[test]
    fn all_themes_have_valid_colors() {
        for entry in THEMES {
            let theme = entry.theme;
            assert!(
                matches!(theme.background, ratatui::style::Color::Rgb(_, _, _)),
                "Theme {} has invalid background",
                entry.id
            );
            assert!(
                matches!(theme.text, ratatui::style::Color::Rgb(_, _, _)),
                "Theme {} has invalid text color",
                entry.id
            );
        }
    }

    #[test]
    fn theme_ids_are_unique() {
        let ids: Vec<_> = THEMES.iter().map(|e| e.id).collect();
        let mut unique_ids = ids.clone();
        unique_ids.sort_unstable();
        unique_ids.dedup();
        assert_eq!(ids.len(), unique_ids.len(), "Duplicate theme IDs found");
    }

    #[test]
    fn theme_display_names_are_unique() {
        let names: Vec<_> = THEMES.iter().map(|e| e.display_name).collect();
        let mut unique_names = names.clone();
        unique_names.sort_unstable();
        unique_names.dedup();
        assert_eq!(
            names.len(),
            unique_names.len(),
            "Duplicate display names found"
        );
    }

    #[test]
    fn find_theme_matches_all_listed_themes() {
        for id in list_themes() {
            assert!(
                find_theme(id).is_some(),
                "find_theme({id}) returned None but id is in list_themes()"
            );
        }
    }
}
