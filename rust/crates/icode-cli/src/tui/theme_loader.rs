macro_rules! define_theme {
    ($id:ident, $theme_id:expr, $display:expr, $cat:expr, $t:expr, $entry_id:ident) => {
        pub const $id: Theme = $t;
        pub const $entry_id: ThemeEntry = ThemeEntry {
            id: $theme_id,
            display_name: $display,
            category: $cat,
            theme: &$id,
        };
    };
}

